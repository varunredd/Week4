class Datatypes {

    var byteVar: Byte = 10
    var shortVar: Short = 20
    var intVar: Int = 30
    var longVar: Long = 40L
    var floatVar: Float = 50.5f
    var doubleVar: Double = 60.5
    var charVar: Char = 'A'
    var booleanVar: Boolean = true

    fun display() {
        println("Byte: $byteVar")
        println("Short: $shortVar")
        println("Int: $intVar")
        println("Long: $longVar")
        println("Float: $floatVar")
        println("Double: $doubleVar")
        println("Char: $charVar")
        println("Boolean: $booleanVar")
    }


}
